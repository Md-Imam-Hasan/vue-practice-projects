<template>
  <section class="mt-5 text-center">
    <button class="border rounded px-2" @click="loginWithGoogle">
      Log in With Google
    </button>
  </section>
</template>

<script>
import firebase from "../../utilities/firebase";
export default {
  methods: {
    loginWithGoogle() {
      var provider = new firebase.auth.GoogleAuthProvider();
      firebase
        .auth()
        .signInWithPopup(provider)
        .then(() => {
          this.$emit("openLoginModalFromGoogle");
        });
    },
  },
};
</script>

